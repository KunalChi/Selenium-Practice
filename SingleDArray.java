package com.Arrays;

public class SingleDArray {

	public static void main(String[] args) {
		
		int[] nums=new int [4];
		
		int i;
		
		nums[0]=4;
		
		nums[1]=8;
		
		nums[2]=10;
		
		nums[3]=40;
		
		for(i=0;i<4;i++)
		{
		System.out.println(nums[i]); 
		}
	}
}

	

	
	
