package com.Arrays;


public class PrintMultiplicationtable {
	    
	    public static void main(String[] args) {
	    	
	        
	        	  int number = 9;
	        	  printTable(number);   
	         }
	  		private static void printTable(int n) {
	  			 		    
	  		   for (int j = 1; j <= 10; j ++) { //from 0 to 9 
	  			 System.out.format("%2d x %d = %3d\n", n,j,n*j);

	  	        }
	  	        System.out.println();
        	        }
        	       
	  			}
	  		
	       
	        

	    
	    

	    
	  


	    


